    public static bool SupportsLanguage(string languageCode)
    {
        return Map.ContainsKey(NormalizeLanguageCode(languageCode));
    }

    public static string Get(string key, string languageCode)
    {
        var lang = NormalizeLanguageCode(languageCode);

        if (Map.TryGetValue(lang, out var table) && table.TryGetValue(key, out var s))
            return s;

        if (lang != "en" && Map.TryGetValue("en", out var en) && en.TryGetValue(key, out s))
            return s;

        if (Map.TryGetValue("vi", out var vi) && vi.TryGetValue(key, out s))
            return s;

        return key;
    }

    private static string NormalizeLanguageCode(string? languageCode)
    {
        if (string.IsNullOrWhiteSpace(languageCode))
            return "vi";

        var code = languageCode.Trim().ToLowerInvariant();
        if (code.StartsWith("zh"))
            return "zh";
        if (code.StartsWith("ja"))
            return "ja";
        if (code.StartsWith("ko"))
            return "ko";
        if (code.StartsWith("en"))
            return "en";
        if (code.StartsWith("vi"))
            return "vi";
        return code;
    }
