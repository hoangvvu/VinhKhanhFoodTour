    public static string Get(string key, string languageCode)
    {
        var lang = string.IsNullOrWhiteSpace(languageCode) ? "vi" : languageCode.Trim().ToLowerInvariant();

        if (Map.TryGetValue(lang, out var table) && table.TryGetValue(key, out var s))
            return s;

        if (lang != "en" && Map.TryGetValue("en", out var en) && en.TryGetValue(key, out s))
            return s;

        if (Map.TryGetValue("vi", out var vi) && vi.TryGetValue(key, out s))
            return s;

        return key;
    }
